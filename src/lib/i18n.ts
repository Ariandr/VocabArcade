export type TranslationKey = keyof typeof english;
export type TranslationParams = Record<string, string | number>;

const translations: Record<AppLocale, Record<TranslationKey, string>> = {
  en: english,
  uk: ukrainian,
  pl: polish,
};

export function normalizeAppLocale(value: unknown): AppLocale {
  return value === "uk" || value === "en" || value === "pl" ? value : DEFAULT_APP_LOCALE;
}

export function loadAppLocale(): AppLocale {
  return normalizeAppLocale(localStorage.getItem(APP_LOCALE_KEY));
}

export function saveAppLocale(locale: AppLocale): void {
  localStorage.setItem(APP_LOCALE_KEY, normalizeAppLocale(locale));
}

export function translate(
  locale: AppLocale,
  key: TranslationKey,
  params: TranslationParams = {},
): string {
  const template = translations[locale]?.[key] ?? translations.en[key] ?? key;
  return template.replace(/\{(\w+)\}/g, (match, name: string) =>
    Object.prototype.hasOwnProperty.call(params, name) ? String(params[name]) : match,
  );
}
